const { Telegraf } = require("telegraf");
const fs = require("fs");

// –ó–∞–≥—Ä—É–∂–∞–µ–º —Å–ø–∏—Å–∫–∏
const spells = fs.readFileSync("spells.txt", "utf-8").split("\n").filter(Boolean);
const potions = fs.readFileSync("potions.txt", "utf-8").split("\n").filter(Boolean);

// –ü–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —Ç–µ–∫—É—â–∏—Ö –∑–Ω–∞—á–µ–Ω–∏–π
let lastSpell = null;
let lastPotion = null;
let currentSpell = null;
let currentPotion = null;

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è —É–Ω–∏–∫–∞–ª—å–Ω–æ–≥–æ —Å–ª—É—á–∞–π–Ω–æ–≥–æ –≤—ã–±–æ—Ä–∞
function getRandomUnique(list, last) {
  let randomItem;
  let attempts = 0;
  const maxAttempts = 10;

  do {
    randomItem = list[Math.floor(Math.random() * list.length)];
    attempts++;
  } while (randomItem === last && attempts < maxAttempts);

  return randomItem;
}

// –û–±–Ω–æ–≤–ª—è–µ–º –∑–Ω–∞—á–µ–Ω–∏—è –∫–∞–∂–¥—ã–µ 5 —Å–µ–∫—É–Ω–¥
function updateRandomValues() {
  currentSpell = getRandomUnique(spells, lastSpell);
  currentPotion = getRandomUnique(potions, lastPotion);
  lastSpell = currentSpell;
  lastPotion = currentPotion;
  console.log(`–û–±–Ω–æ–≤–ª–µ–Ω–æ: ${currentSpell}, ${currentPotion}`);
}

// –ó–∞–ø—É—Å–∫–∞–µ–º –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∫–∞–∂–¥—ã–µ 5 —Å–µ–∫—É–Ω–¥
updateRandomValues(); // —Å—Ä–∞–∑—É –ø–µ—Ä–≤—ã–π —Ä–∞–∑
setInterval(updateRandomValues, 5000);

// –°–æ–∑–¥–∞—ë–º –±–æ—Ç–∞
const bot = new Telegraf(process.env.BOT_TOKEN);

// –ö–æ–º–∞–Ω–¥–∞ /start
bot.start((ctx) => {
  ctx.reply("–ü—Ä–∏–≤–µ—Ç! –ù–∞–ø–∏—à–∏ @XogsmidBot, —á—Ç–æ–±—ã –æ—Ç–∫—Ä—ã—Ç—å –º–µ–Ω—é —Å –∑–∞–∫–ª–∏–Ω–∞–Ω–∏—è–º–∏ –∏ –∑–µ–ª—å—è–º–∏.");
});

// –û–±—Ä–∞–±–æ—Ç—á–∏–∫ inline-–∑–∞–ø—Ä–æ—Å–∞
bot.inlineQuery(/.*/, (ctx) => {
  const results = [
    {
      type: "article",
      id: "spell",
      title: "–°–ª—É—á–∞–π–Ω–æ–µ –∑–∞–∫–ª–∏–Ω–∞–Ω–∏–µ",
      input_message_content: {
        message_text: `ü™Ñ ${currentSpell || "–ó–∞–∫–ª–∏–Ω–∞–Ω–∏–µ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ"}`,
      },
    },
    {
      type: "article",
      id: "potion",
      title: "–°–ª—É—á–∞–π–Ω–æ–µ –∑–µ–ª—å–µ",
      input_message_content: {
        message_text: `üß™ ${currentPotion || "–ó–µ–ª—å–µ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ"}`,
      },
    },
  ];

  ctx.answerInlineQuery(results);
});

// –ó–∞–ø—É—Å–∫ –±–æ—Ç–∞
const PORT = process.env.PORT || 10000;
const DOMAIN = process.env.RENDER_EXTERNAL_URL;

bot.launch({
  webhook: {
    host: "0.0.0.0",
    port: PORT,
    path: "/bot",
    domain: DOMAIN,
  },
});

console.log(`–ë–æ—Ç –∑–∞–ø—É—â–µ–Ω –Ω–∞ –ø–æ—Ä—Ç—É ${PORT}`);
